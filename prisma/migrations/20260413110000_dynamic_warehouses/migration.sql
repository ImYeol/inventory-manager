create table if not exists public.warehouses (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  name text not null,
  created_at timestamptz not null default timezone('utc', now()),
  unique (user_id, name)
);

create index if not exists warehouses_user_id_created_at_idx on public.warehouses (user_id, created_at desc);
create unique index if not exists warehouses_id_user_id_uidx on public.warehouses (id, user_id);

insert into public.warehouses (user_id, name)
select distinct i.user_id, '오금동'
from public.inventory i
where i.warehouse = 'OGEUMDONG'
and not exists (
  select 1 from public.warehouses w
  where w.user_id = i.user_id and w.name = '오금동'
);

insert into public.warehouses (user_id, name)
