alter table public.inventory add column if not exists warehouse_id bigint;
alter table public.transactions add column if not exists warehouse_id bigint;

update public.inventory i
set warehouse_id = w.id
from public.warehouses w
where i.user_id = w.user_id
  and ((i.warehouse = 'OGEUMDONG' and w.name = '오금동') or (i.warehouse = 'DAEJADONG' and w.name = '대자동'));

update public.transactions t
set warehouse_id = w.id
from public.warehouses w
where t.user_id = w.user_id
  and ((t.warehouse = 'OGEUMDONG' and w.name = '오금동') or (t.warehouse = 'DAEJADONG' and w.name = '대자동'));

create index if not exists inventory_warehouse_id_idx on public.inventory (user_id, warehouse_id);
