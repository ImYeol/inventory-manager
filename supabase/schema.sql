create or replace function private.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  insert into public.app_users (id, email, full_name)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'name')
  )
  on conflict (id) do update
    set email = excluded.email,
        full_name = coalesce(excluded.full_name, public.app_users.full_name);
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;

create trigger on_auth_user_created
after insert on auth.users
for each row execute procedure private.handle_new_user();

create or replace function public.bulk_apply_inventory_transactions(p_items jsonb)
returns void
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  item jsonb;
  v_model_id bigint;
  v_size_id bigint;
  v_color_id bigint;
  v_quantity integer;
  v_date date;
  v_type public.transaction_type_value;
  v_warehouse public.warehouse_value;
  v_inventory_quantity integer;
begin
  if v_user_id is null then
    raise exception 'Authentication is required.';
  end if;

  if jsonb_typeof(p_items) is distinct from 'array' then
    raise exception 'p_items must be a JSON array.';
  end if;

  for item in select * from jsonb_array_elements(p_items)
  loop
    v_model_id := (item ->> 'model_id')::bigint;
    v_size_id := (item ->> 'size_id')::bigint;
    v_color_id := (item ->> 'color_id')::bigint;
    v_quantity := (item ->> 'quantity')::integer;
    v_type := (item ->> 'type')::public.transaction_type_value;
    v_warehouse := (item ->> 'warehouse')::public.warehouse_value;

    if (item ->> 'date') ~ '^\d{2}\.\d{2}\.\d{2}$' then
      v_date := to_date(item ->> 'date', 'YY.MM.DD');
    else
      v_date := (item ->> 'date')::date;
    end if;

    if v_quantity is null or v_quantity <= 0 then
      raise exception 'quantity must be greater than zero.';
    end if;

    insert into public.transactions (
      user_id, date, model_id, size_id, color_id, type, quantity, warehouse
    )
    values (
      v_user_id, v_date, v_model_id, v_size_id, v_color_id, v_type, v_quantity, v_warehouse
    );

    if v_type = 'INBOUND' then
      insert into public.inventory (
        user_id, model_id, size_id, color_id, warehouse, quantity
      )
      values (
        v_user_id, v_model_id, v_size_id, v_color_id, v_warehouse, v_quantity
      )
      on conflict (user_id, model_id, size_id, color_id, warehouse)
      do update
        set quantity = public.inventory.quantity + excluded.quantity,
            updated_at = timezone('utc', now());
    elsif v_type = 'OUTBOUND' then
      select quantity
      into v_inventory_quantity
      from public.inventory
      where user_id = v_user_id
        and model_id = v_model_id
        and size_id = v_size_id
        and color_id = v_color_id
        and warehouse = v_warehouse
      for update;

      if not found then
        raise exception 'Inventory not found for outbound transaction.';
      end if;

      if v_inventory_quantity < v_quantity then
        raise exception 'Insufficient inventory for outbound transaction.';
      end if;

      update public.inventory
      set quantity = quantity - v_quantity,
          updated_at = timezone('utc', now())
      where user_id = v_user_id
        and model_id = v_model_id
        and size_id = v_size_id
        and color_id = v_color_id
        and warehouse = v_warehouse;
    else
      insert into public.inventory (
        user_id, model_id, size_id, color_id, warehouse, quantity
      )
      values (
        v_user_id, v_model_id, v_size_id, v_color_id, v_warehouse, v_quantity
      )
      on conflict (user_id, model_id, size_id, color_id, warehouse)
      do update
        set quantity = excluded.quantity,
            updated_at = timezone('utc', now());
    end if;
  end loop;
end;
$$;

create or replace function public.apply_inventory_adjustment(
  p_inventory_id bigint,
  p_new_quantity integer
)
returns void
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_inventory public.inventory%rowtype;
begin
  if v_user_id is null then
    raise exception 'Authentication is required.';
  end if;

  if p_new_quantity < 0 then
    raise exception 'Quantity cannot be negative.';
  end if;

  select *
  into v_inventory
  from public.inventory
  where id = p_inventory_id
    and user_id = v_user_id
  for update;

  if not found then
    raise exception 'Inventory not found.';
  end if;

  update public.inventory
  set quantity = p_new_quantity,
      updated_at = timezone('utc', now())
  where id = p_inventory_id
    and user_id = v_user_id;

  insert into public.transactions (
    user_id, date, model_id, size_id, color_id, type, quantity, warehouse
  )
  values (
    v_user_id,
    current_date,
    v_inventory.model_id,
    v_inventory.size_id,
    v_inventory.color_id,
    'ADJUSTMENT',
    p_new_quantity,
    v_inventory.warehouse
  );
end;
$$;
